package com.example.demo.entity;

import jakarta.persistence.*;
import lombok.Getter;
import lombok.NoArgsConstructor;
import lombok.Setter;

import java.time.LocalDate;
import java.time.LocalTime;

/**
 * Entity class representing a meeting room booking.
 * <p>
 * This entity stores booking information including the room ID, date, start
 * time, and end time.
 * Bookings are persisted in the "bookings" table in the database.
 * </p>
 *
 * @author Tharinda Rajapaksha
 * @version 1.0.0
 */
@Entity
@Table(name = "bookings")
@Getter
@Setter
@NoArgsConstructor
public class Booking {

    /**
     * Unique identifier for the booking.
     * Auto-generated by the database.
     */
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    /**
     * The ID of the meeting room being booked.
     * Must be between 1 and 7 (inclusive).
     */
    @Column(nullable = false)
    private Integer roomId;

    /**
     * The date of the booking.
     * Format: YYYY-MM-DD
     */
    @Column(nullable = false)
    private LocalDate date;

    /**
     * The start time of the booking.
     * Format: HH:mm
     */
    @Column(nullable = false)
    private LocalTime startTime;

    /**
     * The end time of the booking.
     * Format: HH:mm
     * Must be after the start time.
     */
    @Column(nullable = false)
    private LocalTime endTime;

    /**
     * Constructor for creating a new booking without an ID.
     * The ID will be auto-generated by the database.
     *
     * @param roomId    the ID of the room to book
     * @param date      the date of the booking
     * @param startTime the start time of the booking
     * @param endTime   the end time of the booking
     */
    public Booking(Integer roomId, LocalDate date, LocalTime startTime, LocalTime endTime) {
        this.roomId = roomId;
        this.date = date;
        this.startTime = startTime;
        this.endTime = endTime;
    }
}
